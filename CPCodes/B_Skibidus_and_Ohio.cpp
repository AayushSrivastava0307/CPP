void solve()
{
 
    string s;
    cin>>s;
    int n=s.size();
    bool check=false;
    for(int i=0;i<n-1;i++){
        if(s[i]==s[i+1]){
            check=true;
            break;
        }
    }
    cout<<((check)?1:n)<<endl;
 
}
 
 
 
signed main()
{
 
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
 
    int tc=1;
    cin>>tc;
 
    while(tc--)
    {
        solve();
    }
    return 0;
}